#include <iostream>
using namespace std;

int main()
{
    // addition +
    cout << 5 + 7 << endl;
    // subtraction -
    cout << 5 - 7 << endl;
    // multiplication *
    cout << 5 * 7 << endl;
    // division /
    cout << 5 / 7.0 << endl;
    // modulus %
    cout << 50 / 7 << endl;

    // increment x++
    // decrement x--

    // casting
    double number = (double)(5 / 2);
    // convert it into a string 
    number = (int)(number);
    cout << number;
}